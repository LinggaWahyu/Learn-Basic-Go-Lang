package main

import "fmt"

func main() {
	var name string

	name = "Lingga"
	fmt.Println(name)

	name = "Lingga Wahyu"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	country = "America"
	fmt.Println(country)

	var (
		firstName = "Lingga"
		lastName = "Wahyu"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
